export function SidebarChevronIcon({
  className,
  expanded,
  pointing,
}: {
  className?: string;
  expanded?: boolean;
  pointing?: "down" | "right" | "left";
}) {
  const transform =
    pointing === "left"
      ? "rotate(90deg)"
      : pointing === "right"
        ? "rotate(-90deg)"
        : pointing === "down"
          ? expanded
            ? "rotate(180deg)"
            : "rotate(0deg)"
          : expanded
            ? "rotate(0deg)"
            : "rotate(-90deg)";

  return (
    <svg
      className={className}
      width={12}
      height={12}
      viewBox="0 0 24 24"
      aria-hidden="true"
      style={{ transform }}
    >
      <polyline
        points="6 9 12 15 18 9"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
      />
    </svg>
  );
}

